//String
let str="hello";
console.log(str);//hello
console.log(typeof str); //string, 'typeof' is used to identify the data type of a variable

//Number
let num=45.67;
console.log(num); //45.67
console.log(typeof num); //number   

//Boolean
let bool=true;
console.log(bool); //true
console.log(typeof bool); //boolean

//Undefined
let undef;
console.log(undef); //undefined
console.log(typeof undef); //undefined

//Null      
let n=null;
console.log(n); //null
console.log(typeof n); //object

//Array
let arr=[1, "text", true, null];
console.log(arr); //[ 1, 'text', true, null ]
console.log(typeof arr); //object (arrays are a type of object in JavaScript)

//Object
let obj={name: "John", age: 30};
console.log(obj); //{ name: 'John', age: 30 }
console.log(typeof obj); //object